#Bitwise Ops are rarely used and only used in specific situations
#Bitwise operators are used to compare binary numbers

# & performs the binary AND - sets each bit to 1 if both bits are 1
# | performs the binary OR - sets each bit to 1 if one of two bits is 1
# ^ performs a binary XOR operation - sets each bit to 1 if only one of two bits is 1
# ~ performs a binary NOT operation - inverts all the bits
# << shift left operation - shift left by pushing zeros in from the right and let the leftmost bits fall off
# >> shift right operation - shift right by pushing copies of the leftmost bit in from the left and let the rightmost bits fall off
