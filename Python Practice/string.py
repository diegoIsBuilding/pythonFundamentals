#A string can be written in single or double quotes
first_name = 'Diego'
last_name = "Garcia"

full_name = first_name + " " + last_name

phrase = f'My name is {full_name}'

age = str(26)

phrase_two = phrase + f' and I am {age} years old.'

print(first_name)
print(last_name)
print(full_name)
print(phrase)
print(age)
print(phrase_two)

#Multi Line String
print('''Diego is
      26
      
      years old''')

