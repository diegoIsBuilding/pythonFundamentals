#The round() function returns a floating point number that is a rounded version of the specified number with
#the specified number of decimals
#The round() function is going to round to the nearest integer
number = 5.5
print(round(number)) #Returns 6
number_two = 5.49
print(round(number_two)) #Returns 5
number_three = 5.76543
print(round(number_three, 2)) #Returns 5.77

